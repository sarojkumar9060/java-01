import java.util.*;
public class Main {
    public static void main(String[] args) {
        
        //Assignment operator assigns the value of RHS to LHS
        
        int x = 100; //100 (RHS) gets assigned to x (LHS)
        int y = x; //The value of x i.e. 100 (RHS) gets assigned to y (LHS)
        
        System.out.println(x);
        System.out.println(y);
    }
}
